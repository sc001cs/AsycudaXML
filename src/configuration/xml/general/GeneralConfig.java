package configuration.xml.general;

public class GeneralConfig {

	private String data_format_file;
	private String utf8;
	private String name_file_excel;
	private String name_file_output;
	private String path_folder;
	
	public String getData_format_file() {
		return data_format_file;
	}
	public void setData_format_file(String data_format_file) {
		this.data_format_file = data_format_file;
	}
	public String getUtf8() {
		return utf8;
	}
	public void setUtf8(String utf8) {
		this.utf8 = utf8;
	}
	public String getName_file_excel() {
		return name_file_excel;
	}
	public void setName_file_excel(String name_file_excel) {
		this.name_file_excel = name_file_excel;
	}
	public String getName_file_output() {
		return name_file_output;
	}
	public void setName_file_output(String name_file_output) {
		this.name_file_output = name_file_output;
	}
	public String getPath_folder() {
		return path_folder;
	}
	public void setPath_folder(String path_folder) {
		this.path_folder = path_folder;
	}
	

	
}
